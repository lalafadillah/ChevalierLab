    // function
    function PrintNamaKamu (nama){
        console.log(nama);
    }
    // arrow function
    const SamaTapiIniArrowFunction = (nama) => {
        console.log(nama);
    }
    

    // Call function and arrow function
    PrintNamaKamu('Rifky Muhammad Prayudhi');
    // Output: Rifky Muhammad Prayudhi
    SamaTapiIniArrowFunction('Rifky Muhammad Prayudhi');
    // Output: Rifky Muhammad Prayudhi